import UIKit

extension UIColor {
    
    struct App {
        static func mainThemeColor() -> UIColor {
            return UIColor(red: 0.255, green: 0.600, blue: 0.565, alpha: 1.00)
        }
        
        static func mainThemeTransparentColor() -> UIColor {
            return UIColor(red: 0.255, green: 0.600, blue: 0.565, alpha: 0.80)
        }
    }
    
}

